package casino;

public class CFGSaldo {

	float saldoCasino;
	float saldoPozoFeliz;

	public CFGSaldo(float saldoCasino, float saldoPozoFeliz) {
		super();
		this.saldoCasino = saldoCasino;
		this.saldoPozoFeliz = saldoPozoFeliz;
	}
	
	public float getSaldoCasino() {
		return saldoCasino;
	}
	public void setSaldoCasino(float saldoCasino) {
		this.saldoCasino = saldoCasino;
	}
	public float getSaldoPozoFeliz() {
		return saldoPozoFeliz;
	}
	public void setSaldoPozoFeliz(float saldoPozoFeliz) {
		this.saldoPozoFeliz = saldoPozoFeliz;
	}
	
	
}
