package parser;

public class MSGMesaTragamonedas {

	private MSGJugador jugador;
	
	private MSGUltimoTiroTragamonedas ultimoTiroTragamonedas;
	
	private String id;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public MSGJugador getJugador() {
		return jugador;
	}

	public void setJugador(MSGJugador jugador) {
		this.jugador = jugador;
	}

	public MSGUltimoTiroTragamonedas getUltimoTiroTragamonedas() {
		return ultimoTiroTragamonedas;
	}

	public void setUltimoTiroTragamonedas(
			MSGUltimoTiroTragamonedas ultimoTiroTragamonedas) {
		this.ultimoTiroTragamonedas = ultimoTiroTragamonedas;
	}
	
	
}
