package core;

@SuppressWarnings("serial")
public class MensajeroException extends Exception {

	public MensajeroException() {
	}

	public MensajeroException(String arg0) {
		super(arg0);
	}

	public MensajeroException(Throwable arg0) {
		super(arg0);
	}

	public MensajeroException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
