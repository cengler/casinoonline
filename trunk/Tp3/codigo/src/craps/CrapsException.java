package craps;

/*
 * 
 */
@SuppressWarnings("serial")
public class CrapsException extends Exception {

	public CrapsException() {
	}

	public CrapsException(String arg0) {
		super(arg0);
	}

	public CrapsException(Throwable arg0) {
		super(arg0);
	}

	public CrapsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
